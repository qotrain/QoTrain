#pragma mark - Table view delegate

- (void)tableView:(UITableView *)tableView didSelectRowAtIndexPath:(NSIndexPath *)indexPath
{
    LeaderboardViewController *leaderboardViewController;
    FindPeopleViewController *findPeopleViewController;

    switch (indexPath.row) {
        case 0:
            leaderboardViewController = [[[LeaderboardViewController alloc] initWithNibName:[[LeaderboardViewController class] description] bundle:nil] autorelease];
            leaderboardViewController.title = [tableView cellForRowAtIndexPath:indexPath].textLabel.text;
            [((qotrainAppDelegate*)[[UIApplication sharedApplication] delegate]).communityNavController pushViewController:leaderboardViewController animated:YES];
            break;
        case 1:
            findPeopleViewController = [[[FindPeopleViewController alloc] initWithNibName:[[FindPeopleViewController class] description] bundle:nil] autorelease];
            findPeopleViewController.title = [tableView cellForRowAtIndexPath:indexPath].textLabel.text;
            [((qotrainAppDelegate*)[[UIApplication sharedApplication] delegate]).communityNavController pushViewController:findPeopleViewController animated:YES];
            break;
        default:
            break;
    }
}

